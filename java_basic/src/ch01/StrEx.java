package ch01;

public class StrEx {
	public static void main(String[] args) {
		String name = "김철수";
		System.out.println("Name: " + name); // + ; 연결자
		System.out.println(10 + 20); // 숫자끼리의 덧셈
		System.out.println("20" + 10); // 문자열과의 연결 ; 자동형변환

	}
}
