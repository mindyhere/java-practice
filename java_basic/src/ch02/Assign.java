package ch02;

public class Assign {
	public static void main(String[] args) {
		int a = 10, b = 20;
		// "," -> 연속작업
		a += 5; // a= a+5 을 가단하게 표현
		System.out.println(a);
		a -= 5; // 15-5 를 의미
		System.out.println(a);
		b *= 5; // 10*5 를 ㅢ미
		System.out.println(b);
		b /= 2; // 100/2 를 의미
		System.out.println(b);
	}
}
